    private void buscarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotonActionPerformed
        // TODO add your handling code here:
        try {
            String textoBuscado = busquedaUsuario.getText();
            usuario[] usuariosFiltrados = u.buscarUsuarios(textoBuscado);

            String[] listaUsuarios = new String[30];
            int index = 0;
            for (usuario user : usuariosFiltrados) {
                if (user != null) {
                    String usuarioA = user.usuario; 
                    String seguimiento = u.comprobacionSeguimiento(usuarioA);

                    if (index < listaUsuarios.length) {
                        listaUsuarios[index] = usuarioA + "       " + seguimiento;
                        index++;
                    }
                }
            }
            if (index > 0) {
                listaBusqueda.setListData(listaUsuarios);
            } else {
                JOptionPane.showMessageDialog(null, "No se encontraron usuarios!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ingrese los datos adecuados para cada espacio.");
        }
    }//GEN-LAST:event_buscarBotonActionPerformed

