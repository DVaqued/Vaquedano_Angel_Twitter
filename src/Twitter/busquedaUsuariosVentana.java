    private void listaBusquedaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listaBusquedaMouseClicked
        // TODO add your handling code here:
        try {
            usuario u = new usuario();
            
            String nombreSeleccionado = listaBusqueda.getSelectedValue();
            String nombreSeleccionadoModificado = nombreSeleccionado.split(" ")[0].trim();
            
            u.asignarNombre(nombreSeleccionadoModificado);
            u.asignarUsuario(nombreSeleccionadoModificado);
            u.asignarCantidadSeguidores(nombreSeleccionadoModificado);
            u.asignarCantidadSeguidos(nombreSeleccionadoModificado);
            u.asignarEdad(nombreSeleccionadoModificado);
            u.asignarGenero(nombreSeleccionadoModificado);
            perfilUsuario pU = new perfilUsuario();
            pU.setVisible(true);
            this.dispose();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "El espacio seleccionado no existe!");
        }
    }//GEN-LAST:event_listaBusquedaMouseClicked

